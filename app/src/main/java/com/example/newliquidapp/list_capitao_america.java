package com.example.newliquidapp;

public class list_capitao_america {

    private int id;
    private int imageCapitao;

    public list_capitao_america(int id, int imageCapitao) {
        this.id = id;
        this.imageCapitao = imageCapitao;
    }

    public int getId() {
        return id;
    }

    public int getImageCapitao() {
        return imageCapitao;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setImageCapitao(int imageCapitao) {
        this.imageCapitao = imageCapitao;
    }
}
